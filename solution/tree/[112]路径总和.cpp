/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode(int x) : val(x), left(NULL), right(NULL) {}
 * };
 */
class Solution {
public:
    bool hasPathSum(TreeNode* root, int sum) {

        return pathSum(root, 0, sum);
        
    }

    bool pathSum(TreeNode* root, int tmpSum, int sum) {

        if(root) {
            tmpSum += root->val;

            if(tmpSum == sum && root->left == nullptr && root->right == nullptr) {
                return true;
            }

            return pathSum(root->left, tmpSum, sum);
            return pathSum(root->right, tmpSum, sum);
        }

        return false;
    }

};
